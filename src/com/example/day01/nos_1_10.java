package com.example.day01;

public class nos_1_10
{
    public static void main(String[] args) {
        System.out.println("Nos ranging from 1 to 10 :\n**************************");
        for(int i =1; i<=10; i++) {
            System.out.print(i+"  ");
        }
        System.out.println();

        for(int i =10; i>=1; i--) {
            System.out.print(i+"  ");
        }



        }





    }

